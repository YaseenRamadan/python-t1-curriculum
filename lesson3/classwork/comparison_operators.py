# comparison operaters compare values and return true or false
print(5 > 3) # greater than
print(7 < 2) # less than
print(5 >= 5) # greater or egual to
print(4 <= 4) # less than or egual to
print(7 == 7) # equal to(double equals, not single)
print(5 != 3) #not equal to

# you can compare variables as well:
a = 10
b = 7
print(a > b)
print(a == b)